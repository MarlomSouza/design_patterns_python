class Computer(object):

    def display(self):
        print("Computer: ")
        print(f"CASE: {self.case}")
        print(f"MAINBOARD: {self.mainboard}")
        print(f"CPU: {self.cpu}")
        print(f"MEMORY: {self.memory}")
        print(f"HARD DRIVE: {self.hard_drive}")
        print(f"VIDEO CARD: {self.video_card}")
